    /**
     * function that creates and loads QRCode from address
     */
    public void loadQRCode() {
        try {
            File tmpDir = new File("tmp");
            if( !tmpDir.exists()) {
                tmpDir.mkdir();
            }
            //String qrcodeFilePath = "tmp" + File.separator + "qrcode.png";
            qrcodeFilePath = "tmp" + File.separator + "qrcode"+count+".png";
            if( count > 0 ) {
                String oldQrcodeFilePath = "tmp" + File.separator + "qrcode"+(count-1)+".png";
                File file = new File(oldQrcodeFilePath);
                if( file.exists() )
                    file.delete();
            }
            String charset = "UTF-8";
            Map hintMap = new HashMap();
            hintMap.put(EncodeHintType.ERROR_CORRECTION, ErrorCorrectionLevel.L);
            QRCodeUtil.createQRCode("bitcoin:"+address, qrcodeFilePath, charset, hintMap, 200, 200);
            lblQrcode.setIcon(new ImageIcon(qrcodeFilePath));
            count++;
        }catch( Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public String getQrcodeFilePath() {
        return qrcodeFilePath;
    }

